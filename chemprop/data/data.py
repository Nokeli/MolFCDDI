class MoleculeDatapoint:
    """A MoleculeDatapoint contains a single molecule and its associated features and targets."""

    def __init__(self,
                 line: List[str],
                 args: Namespace = None,
                 features: np.ndarray = None,
                 use_compound_names: bool = False):
        """
        Initializes a MoleculeDatapoint, which contains a single molecule.

        :param line: A list of strings generated by separating a line in a data CSV file by comma.
        :param args: Arguments.
        :param features: A numpy array containing additional features (ex. Morgan fingerprint).
        :param use_compound_names: Whether the data CSV includes the compound name on each line.
        """
        if args is not None:
            self.features_generator = args.features_generator
            self.args = args
        else:
            self.features_generator = self.args = None

        if features is not None and self.features_generator is not None:
            raise ValueError('Currently cannot provide both loaded features and a features generator.')

        self.features = features

        if use_compound_names:
            self.compound_name = line[0]  # str
            line = line[1:]
        else:
            self.compound_name = None

        self.smiles = line[0]  # str
        self.mol = Chem.MolFromSmiles(self.smiles)
        

        # Generate additional features if given a generator
        if self.features_generator is not None:
            self.features = []

            for fg in self.features_generator:
                features_generator = get_features_generator(fg)
                if self.mol is not None and self.mol.GetNumHeavyAtoms() > 0:
                    self.features.extend(features_generator(self.mol))

            self.features = np.array(self.features)

        # Fix nans in features
        if self.features is not None:
            replace_token = 0
            self.features = np.where(np.isnan(self.features), replace_token, self.features)

        # Create targets
        self.targets = [float(x) if x != '' else None for x in line[1:]]
    

    def set_features(self, features: np.ndarray):
        """
        Sets the features of the molecule.

        :param features: A 1-D numpy array of features for the molecule.
        """
        self.features = features

    def num_tasks(self) -> int:
        """
        Returns the number of prediction tasks.

        :return: The number of tasks.
        """
        return len(self.targets)

    def set_targets(self, targets: List[float]):
        """
        Sets the targets of a molecule.

        :param targets: A list of floats containing the targets.
        """
        self.targets = targets

class MoleculeDatapoint_ddi:
    """A MoleculeDatapoint contains a pair of molecules (SMILES) and their associated label."""

    def __init__(self,
                 line: List[str],
                 args: Namespace = None,
                 features: np.ndarray = None,
                 use_compound_names: bool = False):
        """
        Initializes a MoleculeDatapoint, which contains a pair of molecules and their label.

        :param line: A list of strings generated by separating a line in a DDI CSV file by commas.
        :param args: Arguments (optional).
        :param features: A numpy array containing additional features (optional).
        :param use_compound_names: Whether the data CSV includes compound names (optional).
        """
        if args is not None:
            self.features_generator = args.features_generator  # 获取特征生成器
            self.args = args  # 保存参数
        else:
            self.features_generator = self.args = None

        self.features = features  # 存储特征数据

        # DDI格式包含两个SMILES（smile1, smile2）和一个label
        self.smiles1 = line[0]  # 第一个分子（smiles1）
        self.smiles2 = line[1]  # 第二个分子（smiles2）
        self.label = float(line[2]) if line[2] != '' else None  # 目标label（如0或1）

        # 使用RDKit生成每个SMILES对应的分子对象
        self.mol1 = Chem.MolFromSmiles(self.smiles1)  # 将smiles1转换为分子对象
        self.mol2 = Chem.MolFromSmiles(self.smiles2)  # 将smiles2转换为分子对象

        # 如果提供了特征生成器，则根据分子生成额外的特征
        if self.features_generator is not None:
            self.features = []  # 初始化特征列表

            for fg in self.features_generator:  # 遍历特征生成器
                features_generator = get_features_generator(fg)  # 获取特定的特征生成器
                if self.mol1 is not None and self.mol1.GetNumHeavyAtoms() > 0:
                    self.features.extend(features_generator(self.mol1))  # 为第一个分子生成特征并添加到特征列表中
                if self.mol2 is not None and self.mol2.GetNumHeavyAtoms() > 0:
                    self.features.extend(features_generator(self.mol2))  # 为第二个分子生成特征并添加到特征列表中

            self.features = np.array(self.features)  # 将特征转换为numpy数组

        # 处理特征中的NaN值，将其替换为0
        if self.features is not None:
            replace_token = 0  # 用0来替换NaN
            self.features = np.where(np.isnan(self.features), replace_token, self.features)
    def get_mol_graph(self, mol_id: int, args: Namespace, pretrain: bool, cache: Dict = None):
        """
        获取分子图,支持缓存机制
        
        :param mol_id: 1 for mol1, 2 for mol2
        :param args: 参数
        :param pretrain: 是否预训练模式
        :param cache: 缓存字典
        :return: MolGraph对象
        """
        smiles = self.smiles1 if mol_id == 1 else self.smiles2
        cached_graph = self._mol_graph1 if mol_id == 1 else self._mol_graph2
        
        # 如果已经缓存过,直接返回
        if cached_graph is not None:
            return cached_graph
        
        # 如果提供了外部缓存,检查外部缓存
        if cache is not None and smiles in cache:
            graph = cache[smiles]
        else:
            # 计算新图
            from your_module import MolGraph  # 替换为实际导入路径
            graph = MolGraph(smiles, args, pretrain, brics2emb=None)
            
            # 存入外部缓存
            if cache is not None:
                cache[smiles] = graph
        
        # 存入实例缓存
        if mol_id == 1:
            self._mol_graph1 = graph
        else:
            self._mol_graph2 = graph
        
        return graph
    def set_features(self, features: np.ndarray):
        """Sets the features for the molecule pair."""
        self.features = features  # 设置特征数据

    def num_tasks(self) -> int:
        """Returns the number of tasks (just 1 task for the DDI label)."""
        return 1  # DDI通常只有一个任务：预测分子对之间的相互作用标签

    def set_targets(self, targets: List[float]):
        """Sets the targets (labels) for the molecule pair."""
        self.label = targets[0]  # 设置标签


