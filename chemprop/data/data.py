class MoleculeDataset(Dataset):
    """A MoleculeDataset contains a list of molecule pairs and their associated features and targets."""

    def __init__(self, data: List[MoleculeDatapoint_ddi]):
        """
        Initializes a MoleculeDataset for DDI tasks.
        
        :param data: A list of MoleculeDatapoint_ddi.
        """
        self.data = data
        self.args = self.data[0].args if len(self.data) > 0 else None
        self.scaler = None

    def smiles_pair(self):
        return [(d.smiles1, d.smiles2) for d in self.data]

    def mols_pair(self):
        return [(d.mol1, d.mol2) for d in self.data]

    def features(self) -> List[np.ndarray]:
        if len(self.data) == 0 or self.data[0].features is None:
            return None
        return [d.features for d in self.data]

    def targets(self) -> List[float]:
        return [d.label for d in self.data]

    def num_tasks(self) -> int:
        return self.data[0].num_tasks() if len(self.data) > 0 else None

    def features_size(self) -> int:
        return len(self.data[0].features) if len(self.data) > 0 and self.data[0].features is not None else None

    def shuffle(self, seed: int = None):
        if seed is not None:
            random.seed(seed)
        random.shuffle(self.data)

    def normalize_features(self, scaler=None, replace_nan_token: int = 0):
        if len(self.data) == 0 or self.data[0].features is None:
            return None

        if scaler is not None:
            self.scaler = scaler
        elif self.scaler is None:
            from sklearn.preprocessing import StandardScaler
            features = np.vstack([d.features for d in self.data])
            self.scaler = StandardScaler()
            self.scaler.fit(features)

        for d in self.data:
            d.set_features(self.scaler.transform(d.features.reshape(1, -1))[0])

        return self.scaler

    def set_targets(self, targets: List[float]):
        assert len(self.data) == len(targets)
        for i in range(len(self.data)):
            self.data[i].set_targets([targets[i]])

    def __len__(self) -> int:
        return len(self.data)

    def __getitem__(self, item):
        return self.data[item]

class CachedMolGraphCollator:
    """
    带缓存机制的Collate函数,用于DDI数据的批处理
    在collate过程中计算并缓存molecular graph
    """
    
    def __init__(self, args: Namespace, pretrain: bool = False, cache_size: int = 10000):
        """
        :param args: 参数
        :param pretrain: 是否预训练模式
        :param cache_size: 缓存大小(SMILES数量)
        """
        self.args = args
        self.pretrain = pretrain
        self.cache_size = cache_size
        
        # 使用字典作为LRU缓存,键是SMILES,值是MolGraph
        self._graph_cache = {}
        self._cache_order = []  # 用于LRU管理
        
    def _get_or_create_graph(self, smiles: str):
        """
        获取或创建分子图,带LRU缓存
        
        :param smiles: SMILES字符串
        :return: MolGraph对象
        """
        # 检查缓存
        if smiles in self._graph_cache:
            # 更新LRU顺序
            self._cache_order.remove(smiles)
            self._cache_order.append(smiles)
            return self._graph_cache[smiles]
        
        # 创建新图
     # 替换为实际导入路径
        mol_graph = MolGraph(smiles, self.args, self.pretrain, brics2emb=None)
        
        # 添加到缓存
        self._graph_cache[smiles] = mol_graph
        self._cache_order.append(smiles)
        
        # LRU清理:如果缓存超过限制,删除最旧的
        if len(self._cache_order) > self.cache_size:
            oldest_smiles = self._cache_order.pop(0)
            del self._graph_cache[oldest_smiles]
        
        return mol_graph
    
    def __call__(self, batch: List[MoleculeDatapoint_ddi]) -> Dict:
        """
        批处理函数,在这里计算并缓存molecular graph
        
        :param batch: 一个batch的MoleculeDatapoint_ddi列表
        :return: 包含批处理数据的字典
        """  # 替换为实际导入路径
        
        # 收集所有SMILES
        smiles1_batch = []
        smiles2_batch = []
        mol_graphs1 = []
        mol_graphs2 = []
        
        for item in batch:
            smiles1_batch.append(item.smiles1)
            smiles2_batch.append(item.smiles2)
            
            # 获取或创建分子图(带缓存)
            mol_graph1 = self._get_or_create_graph(item.smiles1)
            mol_graph2 = self._get_or_create_graph(item.smiles2)
            
            mol_graphs1.append(mol_graph1)
            mol_graphs2.append(mol_graph2)
        
        # 构建批量图
        descriptors1 = []  # 如果需要descriptor,在这里添加
        descriptors2 = []
        
        batch_graph1 = BatchMolGraph(mol_graphs1, descriptors1, self.args)
        batch_graph2 = BatchMolGraph(mol_graphs2, descriptors2, self.args)
        
        # 提取标签
        labels = torch.FloatTensor([item.label for item in batch if item.label is not None])
        
        # 提取特征(如果存在)
        features = None
        if batch[0].features is not None:
            features = torch.FloatTensor(np.vstack([item.features for item in batch]))
        
        return {
            'batch_graph1': batch_graph1,
            'batch_graph2': batch_graph2,
            'labels': labels,
            'features': features,
            'smiles1': smiles1_batch,
            'smiles2': smiles2_batch
        }
    
    def get_cache_stats(self) -> Dict:
        """获取缓存统计信息"""
        return {
            'cache_size': len(self._graph_cache),
            'cache_limit': self.cache_size,
            'cached_smiles': list(self._graph_cache.keys())[:10]  # 显示前10个
        }